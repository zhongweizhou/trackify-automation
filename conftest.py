@pytest.fixture(scope="session")
def driver(
    pytestconfig: pytest.Config,
    environment_profile: EnvironmentProfile,
) -> Generator[Any, None, None]:
    """Create one Appium driver session for the pytest run.

    Yields:
        The live Appium driver instance created by ``AppiumDriverFactory``.
    """
    from utils.driver import AppiumDriverFactory

    platform = os.getenv("PLATFORM", "android")
    config = load_config(platform=platform)
    appium_driver = AppiumDriverFactory(config=config).create()
    pytestconfig.stash[_ACTIVE_DRIVER] = appium_driver
    try:
        partial_context = _execution_context(
            appium_driver,
            environment=environment_profile.environment,
            app_version="unresolved",
        )
        _write_allure_environment(pytestconfig, partial_context)
        app_version = resolve_app_version(appium_driver, config)
        context = _execution_context(
            appium_driver,
            environment=environment_profile.environment,
            app_version=app_version,
        )
        pytestconfig.stash[_EXECUTION_CONTEXT] = context
        _write_allure_environment(pytestconfig, context)
        yield appium_driver
    finally:
        try:
            appium_driver.quit()
        finally:
            del pytestconfig.stash[_ACTIVE_DRIVER]


@pytest.fixture(autouse=True)
def reset_app_state(request: pytest.FixtureRequest) -> Generator[None, None, None]:
    """Wipe Trackify local data before every test for deterministic state.

    Args:
        request: Current pytest request, used to isolate unit tests from Appium.

    Yields:
        None after relaunching the app under test.
    """
    if request.node.get_closest_marker("unit") is not None:
        yield
        return

    driver = request.getfixturevalue("driver")
    platform = str(driver.capabilities.get("platformName", "")).lower()
    app_id = IOS_BUNDLE_ID if platform == "ios" else PKG

    if platform == "ios":
        if os.getenv("IOS_REAL_DEVICE", "false").lower() == "true":
            driver.execute_script("mobile: removeApp", {"bundleId": app_id})
            driver.execute_script(
                "mobile: installApp", {"app": str(Path(os.environ["APP_PATH"]).resolve())}
            )
        else:
            driver.execute_script("mobile: clearApp", {"bundleId": app_id})
    else:
        adb_command = [_adb()]
        if device_udid := os.getenv("DEVICE_UDID"):
            adb_command.extend(["-s", device_udid])
        subprocess.run(
            [*adb_command, "shell", "pm", "clear", app_id],
            check=True,
            timeout=10,
        )

    if hasattr(driver, "activate_app"):
        driver.activate_app(app_id)
    else:
        driver.launch_app()
    yield


