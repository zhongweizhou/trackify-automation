def pytest_collection_modifyitems(items: list[pytest.Item]) -> None:
    """Keep device-free unit checks deterministic and single-attempt."""
    for item in items:
        if item.get_closest_marker("unit") is not None:
            item.add_marker(pytest.mark.flaky(reruns=0))


class FeatureFile(pytest.File):
    """Proxy explicit .feature paths to their pytest-bdd step module."""

    def collect(self) -> list[pytest.Item]:
        """Collect tests generated by the matching step definition module.

        Returns:
            Test items from the matching ``tests/step_defs/*_steps.py`` file.
        """
        step_path = (
            self.path.parent.parent
            / "step_defs"
            / f"{self.path.stem}_steps.py"
        )
        if not step_path.exists():
            raise pytest.UsageError(f"No step definition module found for {self.path}")

        module = pytest.Module.from_parent(self, path=step_path)
        return list(module.collect())


def pytest_collect_file(
    file_path: Path,
    parent: pytest.Collector,
) -> FeatureFile | None:
    """Collect .feature files only when explicitly passed on the CLI.

    Args:
        file_path: Candidate file path.
        parent: Parent pytest collector.

    Returns:
        A feature proxy collector when the path was explicit, otherwise None.
    """
    if file_path.suffix != ".feature":
        return None

    explicit_features = {
        Path(arg).resolve()
        for arg in parent.config.args
        if str(arg).endswith(".feature")
    }
    if file_path.resolve() not in explicit_features:
        return None

    return FeatureFile.from_parent(parent, path=file_path)


